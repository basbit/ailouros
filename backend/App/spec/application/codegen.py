def _build_context_block(
    workspace_root: str | Path,
    document: SpecDocument,
    repo_map_port: Optional[RepoMapPort],
    spec_graph_port: Optional[SpecGraphPort],
) -> str:
    if context_disabled():
        return ""
    repo_map = repo_map_port if repo_map_port is not None else TreeSitterRepoMapAdapter()
    spec_graph = spec_graph_port if spec_graph_port is not None else FilesystemSpecGraphAdapter()
    assembler = CodegenContextAssembler(repo_map=repo_map, spec_graph=spec_graph)
    return assembler.assemble(workspace_root, document)


def _attempt_diff_generation(
    client: LLMClient,
    request: CodegenRequest,
    document: SpecDocument,
    ws_path: Path,
    base_prompt: str,
    spec_hash: str,
    spec_version: int,
    llm_retry_count: int,
) -> tuple[list[str], list[str], Optional[str]]:
    diff_max = _diff_max_retries()
    last_error: Optional[str] = None

    first_target_rel = document.frontmatter.codegen_targets[0]
    first_target_path = ws_path / first_target_rel
    try:
        existing_content = first_target_path.read_text(encoding="utf-8")
    except OSError as exc:
        return [], [], f"cannot read existing file for diff: {exc}"

    diff_prompt = _build_diff_prompt(first_target_rel, existing_content, base_prompt)

    for diff_attempt in range(diff_max):
        try:
            diff_text = client.generate(
                diff_prompt,
                model=request.model_name,
                seed=request.seed,
            )
        except Exception as exc:
            last_error = f"LLM error on diff attempt {diff_attempt + 1}: {exc}"
            continue

        try:
            written_files, sidecar_paths = _write_targets_diff(
                document, ws_path, diff_text, request, spec_hash, spec_version,
                llm_retry_count + diff_attempt,
            )
            return written_files, sidecar_paths, None
        except (DiffParseError, DiffApplyError, CodegenError) as exc:
            last_error = str(exc)
            _log.info(
                "diff attempt %d/%d failed for spec %r: %s",
                diff_attempt + 1,
                diff_max,
                request.spec_id,
                last_error,
            )

    return [], [], last_error


