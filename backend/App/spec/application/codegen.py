def run_codegen(
    workspace_root: str | Path,
    request: CodegenRequest,
    *,
    llm_client: Optional[LLMClient] = None,
    verifiers: tuple[CodeVerifier, ...] = (),
    postmortem_recorder: Optional[Callable] = None,
    postmortem_retriever: Optional[Callable] = None,
    feedback_retriever: Optional[Callable] = None,
    repo_map_port: Optional[RepoMapPort] = None,
    spec_graph_port: Optional[SpecGraphPort] = None,
) -> CodegenOutcome:
    recorder = postmortem_recorder if postmortem_recorder is not None else noop_recorder
    retriever = postmortem_retriever if postmortem_retriever is not None else noop_retriever
    fb_retriever = feedback_retriever if feedback_retriever is not None else noop_feedback_retriever

    client = _resolve_llm_client(llm_client)
    repository = FilesystemSpecRepository(workspace_root)
    ws_path = repository._workspace_root

    document = repository.load(request.spec_id)
    if not document.frontmatter.codegen_targets:
        raise CodegenError(
            f"spec {request.spec_id!r} has no codegen_targets defined"
        )

    _use_n_best = request.use_n_best or document.frontmatter.complexity == "high"
    if _use_n_best:
        import backend.App.spec.application.n_best_codegen as _nbm
        return _nbm.run_n_best_codegen(
            workspace_root,
            request,
            llm_client=client,
            verifiers=verifiers,
            n=request.n_best_n,
            strategy=request.n_best_strategy,
            temperature=request.n_best_temperature,
        )

    contract = document.section("Public Contract")
    behaviour = document.section("Behaviour")
    examples = document.section("Examples")
    spec_hash = document.codegen_hash()
    spec_version = document.frontmatter.version

    context_block = _build_context_block(
        workspace_root, document, repo_map_port, spec_graph_port
    )

    base_prompt = _build_prompt(request.spec_id, contract, behaviour, examples)
    if context_block:
        base_prompt = context_block + "\n" + base_prompt

    first_target_for_feedback = (
        document.frontmatter.codegen_targets[0]
        if document.frontmatter.codegen_targets
        else ""
    )
    feedback_block = fb_retriever(request.spec_id, first_target_for_feedback)
    if feedback_block:
        base_prompt = feedback_block + "\n\n" + base_prompt

    past_failures_block = retriever(request.spec_id)
    if past_failures_block:
        base_prompt = past_failures_block + "\n\n" + base_prompt

    first_target_rel = document.frontmatter.codegen_targets[0]
    first_target_path = ws_path / first_target_rel
    spec_status = document.frontmatter.status

    force_full = request.codegen_mode == "full_file"
    force_diff = request.codegen_mode == "diff"
    effective_mode = choose_mode(
        target_path_exists=first_target_path.is_file(),
        spec_status=spec_status,
        force_full=force_full,
        force_diff=force_diff,
    )

    llm_retry_count = 0
    max_retries = _max_retries()
    attempt_records: list[AttemptRecord] = []
    active_mode = effective_mode
    written_files: list[str] = []
    sidecar_paths: list[str] = []
    generated_text = ""

    if active_mode == "diff":
        written_files, sidecar_paths, diff_error = _attempt_diff_generation(
            client, request, document, ws_path, base_prompt,
            spec_hash, spec_version, llm_retry_count,
        )
        if diff_error is not None:
            _log.info(
                "diff-mode exhausted for spec %r; escalating to full_file. reason: %s",
                request.spec_id,
                diff_error,
            )
            attempt_records.append(
                AttemptRecord(
                    attempt=0,
                    finding_count=0,
                    first_findings=(),
                    mode="diff",
                    diff_apply_error=diff_error,
                )
            )
            active_mode = "full_file"

    if active_mode == "full_file":
        current_prompt = base_prompt
        for attempt in range(max_retries):
            try:
                generated_text = client.generate(
                    current_prompt,
                    model=request.model_name,
                    seed=request.seed,
                )
                break
            except Exception as exc:
                llm_retry_count = attempt + 1
                if attempt + 1 >= max_retries:
                    raise CodegenError(
                        f"LLM generation failed after {max_retries} attempts: {exc}"
                    ) from exc

        written_files, sidecar_paths = _write_targets(
            document, ws_path, generated_text, request, spec_hash, spec_version, llm_retry_count
        )

    if not verifiers:
        return CodegenOutcome(
            spec_id=request.spec_id,
            written_files=tuple(written_files),
            sidecar_paths=tuple(sidecar_paths),
            retry_count=llm_retry_count,
            verification_attempts=tuple(attempt_records),
        )

    written_tuple = tuple(written_files)
    verify_attempt = 1

    while True:
        error_findings = _collect_error_findings(verifiers, ws_path, written_tuple)
        if not error_findings:
            break

        record = AttemptRecord(
            attempt=verify_attempt,
            finding_count=len(error_findings),
            first_findings=error_findings[:3],
            mode=active_mode,
        )
        attempt_records.append(record)

        if verify_attempt >= max_retries:
            recorder(request.spec_id, request.model_name, attempt_records)
            raise CodegenError(
                f"Code verification failed after {max_retries} attempt(s). "
                f"Retry history: "
                + "; ".join(
                    f"attempt {r.attempt}: {r.finding_count} error(s) "
                    f"(e.g. {r.first_findings[0].message!r})"
                    for r in attempt_records
                    if r.finding_count > 0
                )
            )

        diagnostic = RetryDiagnostic(
            attempt=verify_attempt,
            previous_code=generated_text,
            findings=error_findings,
        )
        current_prompt = format_diagnostic(diagnostic) + "\n\n" + base_prompt

        try:
            generated_text = client.generate(
                current_prompt,
                model=request.model_name,
                seed=request.seed,
            )
        except Exception as exc:
            raise CodegenError(
                f"LLM generation failed on verification retry {verify_attempt}: {exc}"
            ) from exc

        written_files, sidecar_paths = _write_targets(
            document,
            ws_path,
            generated_text,
            request,
            spec_hash,
            spec_version,
            llm_retry_count + verify_attempt,
        )
        written_tuple = tuple(written_files)
        verify_attempt += 1

    return CodegenOutcome(
        spec_id=request.spec_id,
        written_files=tuple(written_files),
        sidecar_paths=tuple(sidecar_paths),
        retry_count=llm_retry_count + (verify_attempt - 1),
        verification_attempts=tuple(attempt_records),
    )


