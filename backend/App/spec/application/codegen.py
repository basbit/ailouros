_ENV_STUB = "SWARM_SPEC_CODEGEN_STUB"
_ENV_MAX_RETRIES = "SWARM_SPEC_CODEGEN_MAX_RETRIES"
_ENV_DIFF_MAX_RETRIES = "SWARM_SPEC_CODEGEN_DIFF_MAX_RETRIES"
_DEFAULT_MODEL = "stub"
_DEFAULT_SEED = 0
_MAX_RETRY = 3
_DIFF_MAX_RETRY = 2

_log = logging.getLogger(__name__)


class CodegenError(Exception):
    pass


class NoLLMClientConfigured(CodegenError):
    pass


@dataclass(frozen=True)
class CodegenRequest:
    spec_id: str
    model_name: str = _DEFAULT_MODEL
    seed: int = _DEFAULT_SEED
    codegen_mode: Optional[CodegenMode] = None
    use_n_best: bool = False
    n_best_n: int = 3
    n_best_strategy: str = "lowest_error"
    n_best_temperature: float = 0.7


@dataclass(frozen=True)
class AttemptRecord:
    attempt: int
    finding_count: int
    first_findings: tuple[VerificationFinding, ...]
    mode: CodegenMode = "full_file"
    diff_apply_error: Optional[str] = None


@dataclass(frozen=True)
class CodegenOutcome:
    spec_id: str
    written_files: tuple[str, ...]
    sidecar_paths: tuple[str, ...]
    retry_count: int
    verification_attempts: tuple[AttemptRecord, ...] = field(default_factory=tuple)


def _build_prompt(spec_id: str, contract: str, behaviour: str, examples: str) -> str:
    return (
        f"# Codegen target: {spec_id}\n\n"
        f"## Public Contract\n{contract}\n\n"
        f"## Behaviour\n{behaviour}\n\n"
        f"## Examples\n{examples}\n"
    )


def _build_diff_prompt(
    target_path: str,
    existing_content: str,
    spec_prompt: str,
) -> str:
    return (
        f"Return a unified diff against the EXISTING file content shown below. "
        f"Do not return a whole file. Use @@ hunk markers only.\n\n"
        f"## Target path\n\n{target_path}\n\n"
        f"## Current file content\n\n{existing_content}\n\n"
        f"## Spec\n\n{spec_prompt}"
    )


def _utc_now_iso() -> str:
    return datetime.now(tz=timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


def _stub_generate(prompt: str, *, model: str, seed: int) -> str:
    return f"# generated by stub\n# model={model} seed={seed}\n# prompt_len={len(prompt)}\n"


class _StubLLMClient:
    def generate(self, prompt: str, *, model: str, seed: int) -> str:
        return _stub_generate(prompt, model=model, seed=seed)


def _resolve_llm_client(client: Optional[LLMClient]) -> LLMClient:
    if client is not None:
        return client
    if os.environ.get(_ENV_STUB, "").strip() == "1":
        return _StubLLMClient()
    raise NoLLMClientConfigured(
        f"No LLM client provided and {_ENV_STUB} is not set to '1'. "
        "Pass an LLMClient or set SWARM_SPEC_CODEGEN_STUB=1 for testing."
    )


def _max_retries() -> int:
    raw = os.environ.get(_ENV_MAX_RETRIES, "").strip()
    if raw.isdigit():
        return int(raw)
    return _MAX_RETRY


def _diff_max_retries() -> int:
    raw = os.environ.get(_ENV_DIFF_MAX_RETRIES, "").strip()
    if raw.isdigit():
        return int(raw)
    return _DIFF_MAX_RETRY


def _write_targets(
    document: SpecDocument,
    ws_path: Path,
    generated_text: str,
    request: CodegenRequest,
    spec_hash: str,
    spec_version: int,
    retry_count: int,
) -> tuple[list[str], list[str]]:
    written_files: list[str] = []
    sidecar_paths: list[str] = []
    generated_at = _utc_now_iso()

    for target_rel in document.frontmatter.codegen_targets:
        target_path = ws_path / target_rel
        try:
            target_path.relative_to(ws_path)
        except ValueError as exc:
            raise CodegenError(
                f"codegen_target {target_rel!r} resolves outside workspace"
            ) from exc

        final_text = generated_text
        if target_path.is_file():
            try:
                existing = target_path.read_text(encoding="utf-8")
                final_text = apply_keep_regions(existing, generated_text)
            except OSError as exc:
                raise CodegenError(
                    f"failed to read existing file {target_path}: {exc}"
                ) from exc

        target_path.parent.mkdir(parents=True, exist_ok=True)
        try:
            target_path.write_text(final_text, encoding="utf-8")
        except OSError as exc:
            raise CodegenError(
                f"failed to write {target_path}: {exc}"
            ) from exc
        written_files.append(target_rel)

        sidecar = ProvenanceSidecar(
            spec_id=request.spec_id,
            spec_version=spec_version,
            spec_hash=spec_hash,
            generated_at=generated_at,
            model=request.model_name,
            seed=request.seed,
            retry_count=retry_count,
            keep_regions=(),
        )
        try:
            sidecar_path = write_sidecar(sidecar, target_path, ws_path)
        except Exception as exc:
            raise CodegenError(
                f"failed to write sidecar for {target_path}: {exc}"
            ) from exc
        sidecar_paths.append(str(sidecar_path.relative_to(ws_path)))

    return written_files, sidecar_paths


def _write_targets_diff(
    document: SpecDocument,
    ws_path: Path,
    diff_text: str,
    request: CodegenRequest,
    spec_hash: str,
    spec_version: int,
    retry_count: int,
) -> tuple[list[str], list[str]]:
    written_files: list[str] = []
    sidecar_paths: list[str] = []
    generated_at = _utc_now_iso()

    parsed = parse_unified_diff(diff_text)
    if not parsed.hunks:
        raise DiffParseError(0, "LLM response contained no hunk markers; expected a unified diff")

    for target_rel in document.frontmatter.codegen_targets:
        target_path = ws_path / target_rel
        try:
            target_path.relative_to(ws_path)
        except ValueError as exc:
            raise CodegenError(
                f"codegen_target {target_rel!r} resolves outside workspace"
            ) from exc

        try:
            existing_content = target_path.read_text(encoding="utf-8")
        except OSError as exc:
            raise CodegenError(
                f"diff mode requires existing file but failed to read {target_path}: {exc}"
            ) from exc

        patched = apply_diff(existing_content, parsed)
        final_text = apply_keep_regions(existing_content, patched)

        target_path.parent.mkdir(parents=True, exist_ok=True)
        try:
            target_path.write_text(final_text, encoding="utf-8")
        except OSError as exc:
            raise CodegenError(
                f"failed to write {target_path}: {exc}"
            ) from exc
        written_files.append(target_rel)

        sidecar = ProvenanceSidecar(
            spec_id=request.spec_id,
            spec_version=spec_version,
            spec_hash=spec_hash,
            generated_at=generated_at,
            model=request.model_name,
            seed=request.seed,
            retry_count=retry_count,
            keep_regions=(),
        )
        try:
            sidecar_path = write_sidecar(sidecar, target_path, ws_path)
        except Exception as exc:
            raise CodegenError(
                f"failed to write sidecar for {target_path}: {exc}"
            ) from exc
        sidecar_paths.append(str(sidecar_path.relative_to(ws_path)))

    return written_files, sidecar_paths


def _collect_error_findings(
    verifiers: tuple[CodeVerifier, ...],
    ws_path: Path,
    written: tuple[str, ...],
) -> tuple[VerificationFinding, ...]:
    all_findings: list[VerificationFinding] = []
    for verifier in verifiers:
        all_findings.extend(verifier.verify(ws_path, written))
    return tuple(f for f in all_findings if f.severity == "error")


